# Integer to float
puts 1.to_f

# Float to integer
puts 1.0.to_i

# String to integer
puts "1".to_i

# String to float
puts "1".to_f

# Integer to string
puts 1.to_s

# Float to string
puts 1.0.to_s

# Summary
# to_f: Converts to float
# to_i: Converts to integer
# to_s: Converts to string
# to_a: Converts to array
# to_h: Converts to hash
# to_sym: Converts to symbol
# to_c : Converts to complex number
# to_r: Converts to rational number
